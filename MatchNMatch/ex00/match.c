int	match(char *s1, char *s2)
{
	while (*s2)
	{
		if (!*s1)
			return (0);
		if (*s2 == '*')
		{
			s1++;
			while (*s2 == '*')
			{
				s2++;
				if (!*s2)
					return (1);
			}
		}
		if (*s2 == *s1 || !*s2)
		{
			s2++;
			s1++;
			if (!*s2 && *s1)
				return (0);
		}
		else
			return (0);
	}
	return (1);
}
